package calc;

public class Calc
    {
        public Calc(){}
        
        int a;
        int b;
 
        public static int soma(int a, int b)
        {
            return  (a + b);
        }
        
        public static int sub(int a, int b)
        {
            return (a - b);
        }
    
        public static int mult(int a, int b)
        {
            return  (a * b);
        }
        
        public static int div(int a, int b)
        {
            return  (a / b);
        }
    }
